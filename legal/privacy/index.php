<h2 id="1a">1a. Quelles informations recueillons-nous ?</h2>
<p>Chaque seconde, au moins une information personnelle concernant quelqu’un est collectée. Chez Minteck Projects, nous essayons de collecter le moins d’informations personnelles possibles.</p>
<p>Nous pouvons collecter vos interactions avec le site qui seront utilisées à des fins de statistiques, mais vous pourrez interdire cela.</p>
