<h1 id="2">2. Cookies</h1>
<h2 id="2a">2a. Que sont les cookies ?</h2>
<p>Les cookies sont de petites informations laissées par les sites Web sur votre navigateur. Ils servent le plus souvent à partager des données de pistage entre les différents sites.</p>
<h2 id="2b">2b. Pourquoi nous utilisons des cookies ?</h2>
<p>Les cookies sont requis pour faire fonctionner nos sites. Nous utilisons des cookies pour vous garder connecté à un site Web, et uniquement pour ça.</p>
<p>Nos cookies ne sont pas utilisés pour le pistage.</p>
<h1 id="3">3. Sécurité</h1>
<h2 id="3a">3a. Stockage sécurisé</h2>
<p>En utilisant nos services, nous stockons vos données dans des espaces de stockage sécurisés. Ces espaces sécurisés ne sont pas accessibles au public, et leurs données sont vérifiées tous les mois.</p>
<p>Nous verifions chaque requête à nos serveurs, et bloquons toute tentative d'intrusion.</p>
<h2 id="3b">3b. Récupérez vos données</h2>
<p>Certaines personnes peuvent vouloir savoir quelles données nous avons les concernant. Si vous voulez obtenir une copie de vos données, demander leur suppression, ou les modifier ; envoyez un courrier électronique à <a href="mailto:minteck.projects+dataremove@gmail.com">minteck.projects+dataremove@gmail.com</a>, nous vous répondrons dans environ 1 semaine.</p>
<p>Vous ne pouvez pas récupérer les données stockées dans les journaux, mais vous pourrez récupérer votre Base de Données Utilisateur PinPages, ou votre Stockage du Compte Minteck Projects.</p>
<h2 id="3c">3c. Demande légale</h2>
<p>Si vos données sont demandées pour des actions légales importantes, nous leur donneront uniquement s’ils respectent la politique de confidentialité en vigueur.</p>
<h2 id="3d">3d. Télémétrie</h2>
